export const fetchData = async (url: string, method: 'get' | 'post' | 'put' | 'delete', body?: object) => {
  const response = await fetch(url, {
    method,
    body: typeof body === 'object' ? JSON.stringify(body) : body,
  });

  return response.json();
};
